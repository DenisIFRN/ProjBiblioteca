     */
    public function store(Request $request)
    {
        $user = new User;
        $user->nome = $request->nome;
        $user->type = 'Aluno';
        $user->matricula = $request->matricula;
        $user->email = $request->email;
        $user->password = Hash::make($request->password);

        $user->save();
        return view('paginas.login.login');
    }

